package baylinux.sagliktest;

public class PrefDBInformation {

	protected String prefDatabase;
	protected String prefHost;
	protected String prefPort;
	protected String prefSchema;
	protected String prefUsername;
	protected String prefPassword;
	public String getPrefDatabase() {
		return prefDatabase;
	}
	public void setPrefDatabase(String prefDatabase) {
		this.prefDatabase = prefDatabase;
	}
	public String getPrefHost() {
		return prefHost;
	}
	public void setPrefHost(String prefHost) {
		this.prefHost = prefHost;
	}
	public String getPrefPort() {
		return prefPort;
	}
	public void setPrefPort(String prefPort) {
		this.prefPort = prefPort;
	}
	public String getPrefSchema() {
		return prefSchema;
	}
	public void setPrefSchema(String prefSchema) {
		this.prefSchema = prefSchema;
	}
	public String getPrefUsername() {
		return prefUsername;
	}
	public void setPrefUsername(String prefUsername) {
		this.prefUsername = prefUsername;
	}
	public String getPrefPassword() {
		return prefPassword;
	}
	public void setPrefPassword(String prefPassword) {
		this.prefPassword = prefPassword;
	}
	
}
